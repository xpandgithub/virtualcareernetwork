  <?php if ($new): ?>
    <span class="new"><?php print $new ?></span>
  <?php endif; ?>

  <?php print render($title_prefix); ?>
  <h4<?php print $title_attributes; ?>><?php print $title ?></h4>
  <?php print render($title_suffix); ?>

  <div class="submitted">
    <?php //print $permalink; ?>
