<h1 class="title">Match Your Interests to Careers</h1>

<div>
The Interest Profiler is based on the Holland Occupational Themes (RIASEC) which is a theory of careers 
and vocational choice based upon personality types.  To learn more 
<?php echo vcn_build_link_window_opener('http://en.wikipedia.org/wiki/Holland_Codes', 'click here', false); ?>.
Below describes each theme of the RIASEC along with the color that matches within the Interest Profiler.
<br/><br/><br/>
</div>

<div class="profiler-info-container-outer">
  <div class="profiler-iptable-type-realistic profiler-info-container-left"></div>
  <div class="profiler-info-container-right">
		<strong>Realistic</strong><br/>
		
		People with Realistic interests like work that includes practical, hands-on problems and answers. 
		Often people with Realistic interests do not like careers that involve paperwork or working closely 
		with others.<br/><br/>
		
		They like:<br/>
		<ul>
		  <li>Working with plants and animals</li>
		  <li>Real-world materials like wood, tools, and machinery</li>
		  <li>Outside work</li>
		</ul>
		
  </div>
</div>

<div class="profiler-info-container-outer">
  <div class="profiler-iptable-type-investigative profiler-info-container-left"></div>
  <div class="profiler-info-container-right">
		<strong>Investigative</strong><br/>
		
		People with Investigative interests like work that has to do with ideas 
		and thinking rather than physical activity or leading people.<br/><br/>
		
		They like:<br/>
		<ul>
		  <li>Searching for facts</li>
		  <li>Figuring out problems</li>
		</ul>
    
  </div>
</div>

<div class="profiler-info-container-outer">
  <div class="profiler-iptable-type-artistic profiler-info-container-left"></div>
  <div class="profiler-info-container-right">
		<strong>Artistic</strong><br/>
		
		People with Artistic interests like work that deals with the artistic side of things, such as acting, 
		music, art, and design.<br/><br/>
		
		They like:<br/>
		<ul>
		  <li>Creativity in their work</li>
		  <li>Work that can be done without following a set of rules</li>
		</ul>
		
  </div>
</div>

<div class="profiler-info-container-outer">
  <div class="profiler-iptable-type-social profiler-info-container-left"></div>
  <div class="profiler-info-container-right">
		<strong>Social</strong><br/>
		
		People with Social interests like working with others to help them learn and grow. They like working 
		with people more than working with objects, machines, or information.<br/><br/>
		
		They like:<br/>
		<ul>
			<li>Teaching</li>
			<li>Giving advice</li>
			<li>Helping and being of service to people</li>
		</ul>
		
  </div>
</div>
	  
<div class="profiler-info-container-outer">
  <div class="profiler-iptable-type-enterprising profiler-info-container-left"></div>
  <div class="profiler-info-container-right">
		<strong>Enterprising</strong><br/>
		
		People with Enterprising interests like work that has to do with starting up and carrying out 
		business projects. These people like taking action rather than thinking about things.<br/><br/>
		
		They like:<br/>
		<ul>
			<li>Persuading and leading people</li>
			<li>Making decisions</li>
			<li>Taking risks for profits</li>
		</ul>
		
  </div>
</div>
	  
<div class="profiler-info-container-outer">
  <div class="profiler-iptable-type-conventional profiler-info-container-left"></div>
  <div class="profiler-info-container-right">	  
		<strong>Conventional</strong><br/>
		
		People with Conventional interests like work that follows set procedures and routines. They prefer 
		working with information and paying attention to details rather than working with ideas.<br/><br/>
		
		They like:<br/>
		<ul>
			<li>Working with clear rules</li>
			<li>Following a strong leader</li>
		</ul>
		
  </div>
</div>

